#include "3-calc.h"

/**
 * op_add -  calcsum of two integers
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calc sub for two int
 * @a: first int
 * @b: second int
 * Return: sub of two int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
